#include "board.h"
#include "bsp_uart.h"
#include <stdio.h>
#include "bsp_sht20.h"

int main(void)
{
	
	board_init();
	
	uart1_init(115200U);

	//引脚初始化
	SHT20_GPIO_INIT();
	
    //等待传感器上电初始化完成
    delay_ms(20);
	
	while(1)
	{
        //采集温度
        printf("temp = %.2f\r\n", SHT20_Read(0xf3) );
        //采集湿度
        printf("humi = %.2f\r\n", SHT20_Read(0xf5) );
		printf("\r\n");
        delay_ms(500);
	}
	

}
